class Personaje{

    constructor(nombre = '', alias = '', vida = 0.0, regeneracion = 0.0, danno = 0.0,
        armadura = 0.0, velAtaque = 0.0, resistencia = 0.0, velMovimiento = 0.0)
        {
            this.nombre = nombre;
            this.alias = alias;
            this.vida = vida;
            this.regeneracion = regeneracion;
            this.danno = danno;
            this.armadura = armadura;
            this.velAtaque = velAtaque;
            this.resistencia = resistencia;
            this.velMovimiento = velMovimiento;
        }


}